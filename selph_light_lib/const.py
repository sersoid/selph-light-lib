from math import pi as _pi

# Meter
m = 1.0

km = 1e+3 * m
cm = 1e-2 * m
mm = 1e-3 * m
um = 1e-6 * m
nm = 1e-9 * m

# Speed of light
c = 3e+8

# Hertz
Hz = 1.0

Ghz = 1e+9 * Hz
Mhz = 1e+6 * Hz
khz = 1e+3 * Hz
mhz = 1e-3 * Hz
uhz = 1e-6 * Hz
nhz = 1e-9 * Hz

# Radian
rad = 1.0

mrad = 1e-3 * rad
urad = 1e-6 * rad

# Degrees
deg = (2 * _pi * rad) / 360

# Power
W = 1.0

mW = 1e-3 * W

# Pi
pi = _pi
